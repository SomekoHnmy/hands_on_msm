# ==============================================================================
#  ステップ3：安定化重みを計算する
# ------------------------------------------------------------------------------
#  安定化重み ＝（分子の確率）/（分母の確率）を、各人で時間方向に累積積。
# ==============================================================================
dat$sw <- ave(dat$f_num / dat$f_den, dat$patient_id, FUN = cumprod)

# ==============================================================================
#  ステップ4：1人の患者で重みの意味を確認する
# ------------------------------------------------------------------------------
#  分母 f_den が小さい（＝受けにくい治療状態をあえて取った）ほど、重みが大きい。
#  分子で割ることで、重みが極端に大きくなりすぎないよう安定化している。
# ==============================================================================
cat("=== ステップ4：患者 patient_id=3 の重みの手計算 ===\n")
p3 <- dat[
  dat$patient_id == 3,
  c("month", "hba1c_high", "metformin_high_prev", "metformin_high", "f_den", "f_num", "sw")
]
print(round(p3, 3), row.names = FALSE)
cat("\n")

# ==============================================================================
#  ステップ5：重みの分布を確認し、truncation する
# ------------------------------------------------------------------------------
#  安定化重みは平均が 1 付近・裾が穏やかなのが理想。極端に大きな重みは推定を
#  不安定にするため、上下のパーセンタイル（ここは1%/99%）で切り詰める。
# ==============================================================================
cat("=== ステップ5：重みの分布と truncation ===\n")
cat("  truncation 前:\n")
print(round(summary(dat$sw), 3))
q <- quantile(dat$sw, c(0.01, 0.99))
dat$sw_trunc <- pmin(pmax(dat$sw, q[1]), q[2])
cat(sprintf("\n  1%%/99%% で切り詰め（[%.3f, %.3f]）後:\n", q[1], q[2]))
print(round(summary(dat$sw_trunc), 3))
cat("\n")

# ==============================================================================
#  ステップ6：positivity を各月の有効サンプルサイズ（ESS）で確認する
# ------------------------------------------------------------------------------
#  positivity ＝ どの背景の人にも両方の治療状態がありうること。重み付けで
#  実効的な情報量（ESS）が各月どれだけ残るかを見る。実サンプルに近ければ、
#  極端な重みが少なく positivity は良好。
# ==============================================================================
ess <- function(w) sum(w)^2 / sum(w^2)

cat("=== ステップ6：各月の有効サンプルサイズ（ESS）===\n")
for (m in 1:n_int) {
  dm <- dat[dat$month == m, ]
  cat(sprintf(
    "  月%d: N=%4d  ESS=%4.0f (%3.0f%%)\n", m, nrow(dm), ess(dm$sw_trunc),
    100 * ess(dm$sw_trunc) / nrow(dm)
  ))
}
cat("  → 各月とも ESS が実サンプルに近く、極端な重みは少ない＝positivity は良好。\n\n")


